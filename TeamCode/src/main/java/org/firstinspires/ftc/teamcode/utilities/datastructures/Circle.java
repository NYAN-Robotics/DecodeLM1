package org.firstinspires.ftc.teamcode.utilities.datastructures;

public class Circle {
    public double theX;
    public double theY;
    public double theRadius;

    public Circle(double aX, double aY, double aRadius) {
        this.theX = aX;
        this.theY = aY;
        this.theRadius = aRadius;
    }
}
